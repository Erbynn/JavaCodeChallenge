
package squarenumbers;

public class SquareNumbers {

    public static void main(String[] args) {
 //printing squares of num between 1 to 10
        int sqr=2;
        while( sqr < 10){
            System.out.println( sqr +  " = " + (sqr*sqr) );
            sqr ++;
        }
        
    }
    
}
